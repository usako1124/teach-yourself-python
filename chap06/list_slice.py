data = ['あ', 'い', 'う', 'え', 'お', 'か', 'き', 'く', 'け', 'こ']

print(data[2:5])  # ['う', 'え', 'お']
print(data[2:])  # ['う', 'え', 'お', 'か', 'き', 'く', 'け', 'こ']
print(data[:5])  # ['あ', 'い', 'う', 'え', 'お']
print(data[:])  # ['あ', 'い', 'う', 'え', 'お', 'か', 'き', 'く', 'け', 'こ']
print(data[-7:])  # ['え', 'お', 'か', 'き', 'く', 'け', 'こ']
print(data[-7:-5])  # ['え', 'お']
print(data[::2])  # ['あ', 'う', 'お', 'き', 'け']
print(data[1::2])  # ['い', 'え' 'か', 'く', 'こ']
print(data[::-1])  # ['こ', 'け', 'く', 'き', 'か', 'お', 'え', 'う, 'い', 'あ']
